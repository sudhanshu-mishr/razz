// ─── Product image gradient fallback ─────────────────────────────────────────
export function getProductGradient(name = "", fabric = "") {
  const n = (name + " " + fabric).toLowerCase();
  if (n.includes("lehenga") || n.includes("bridal"))   return "linear-gradient(135deg,#8B0000,#DC143C,#FF6B6B,#FFD700)";
  if (n.includes("saree") || n.includes("sari"))       return "linear-gradient(135deg,#4B0082,#8B008B,#DDA0DD,#FFD700)";
  if (n.includes("kurta") || n.includes("salwar") || n.includes("anarkali")) return "linear-gradient(135deg,#006400,#228B22,#90EE90,#F0FFF0)";
  if (n.includes("sherwani") || n.includes("bandhgala") || n.includes("jodhpuri")) return "linear-gradient(135deg,#FFFFF0,#F5DEB3,#DAA520,#8B6914)";
  if (n.includes("navratri") || n.includes("chaniya") || n.includes("garba")) return "linear-gradient(135deg,#FF4500,#FF6347,#FFD700,#FFA500)";
  if (n.includes("dress") || n.includes("western") || n.includes("coord"))   return "linear-gradient(135deg,#2F4F4F,#708090,#B0C4DE,#F5F5DC)";
  if (n.includes("suit") || n.includes("blazer"))      return "linear-gradient(135deg,#191970,#003087,#4169E1,#87CEEB)";
  if (n.includes("silk") || n.includes("banarasi"))    return "linear-gradient(135deg,#4B0082,#800080,#DA70D6,#FFD700)";
  return "linear-gradient(135deg,#1a1a2e,#16213e,#0f3460,#FF9933)";
}
